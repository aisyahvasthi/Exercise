

def luas_segitiga():
    alas = float(input("Masukkan nilai alas: "))
    tinggi = float(input("Masukkan nilai tinggi: "))
    luas = (alas * tinggi) / 2
    return luas

def keliling_segitiga():
    sisi_1 = float(input("Masukkan nilai sisi pertama: "))
    sisi_2 = float(input("Masukkan nilai sisi kedua: "))
    sisi_3 = float(input("Masukkan nilai sisi ketiga: "))
    keliling = sisi_1 + sisi_2 + sisi_3
    return keliling