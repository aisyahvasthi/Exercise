
def luas_persegi_panjang():
    panjang = float(input("Masukkan nilai sisi panjang: "))
    lebar = float(input("Masukkan nilai sisi lebar: "))
    luas = panjang * lebar
    return luas

def keliling_persegi_panjang():
    panjang = float(input("Masukkan nilai sisi panjang: "))
    lebar = float(input("Masukkan nilai sisi lebar: "))
    keliling = 2 *(panjang * lebar)
    return keliling